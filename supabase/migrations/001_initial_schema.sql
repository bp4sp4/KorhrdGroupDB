create index idx_hakjeom_consultations_created_at on public.hakjeom_consultations using btree (created_at desc);
create index idx_hakjeom_consultations_contact on public.hakjeom_consultations using btree (contact);
create index idx_hakjeom_consultations_click_source on public.hakjeom_consultations using btree (click_source);
create index idx_hakjeom_consultations_status on public.hakjeom_consultations using btree (status);

create trigger update_hakjeom_consultations_updated_at
