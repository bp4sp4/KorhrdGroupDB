-- 학점은행제 상담 테이블 (기존 consultations → hakjeom_consultations)
create table public.hakjeom_consultations (
  id bigserial not null,
  name text not null,
  contact text not null,
  education text null,
  reason text null,
  click_source text null,
  created_at timestamp with time zone null default now(),
  updated_at timestamp with time zone null default now(),
  memo text null,
  status text null default '상담대기'::text,
  subject_cost integer null,
  manager text null,
  residence text null,
  hope_course text null,
